package com.walking.intensive.chapter5.task21;

public class Sphere {
    private final int x;
    private final int y;
    private final int z;
    private final int radius;
    
    public Sphere(int x, int y, int z, int radius) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.radius = radius;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public int getRadius() {
        return radius;
    }
}

